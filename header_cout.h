void find_min_max(int arr[]) {
    int num_e = 10;
    int min_v = arr[0];
    int max_v = min_v;
    for (int i=0; i<num_e; i++){
        if (arr[i] > max_v) {
            max_v = arr[i];
        } else if (arr[i] < min_v) {
            min_v = arr[i];
        }
    }
    cout << "\nMin value: " << min_v;
    cout << "\nMax value: " << max_v;
}
